# Przestudiuj dokumentację opisującą typ int oraz float i napisz kod, w którym
# stworzysz po dwa obiekty typu int i float tak z użyciem różnych wartości dla
# konstruktorów tych typów(np. z inną podstawą niż domyślne 10 dla typu int).

# tworzenie obiektów typu int
int_obj1 = int(10)
int_obj2 = int('110', 2)  # wartość 100 zapisana w systemie dwójkowym

print(int_obj1)
print(int_obj2)

# tworzenie obiektów typu float
float_obj1 = float(3.14)
float_obj2 = float("3.1415")

print(float_obj1)
print(float_obj2)
